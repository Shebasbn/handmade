extern "C" GAME_UPDATE_AND_RENDER(GameUpdateAndRender)
{
    Assert((&Input->Controllers[0].Terminator - &Input->Controllers[0].Buttons[0]) == (ArrayCount(Input->Controllers[0].Buttons)));
    Assert(sizeof(game_state) <= Memory->PermanentStorageSize);
    
    game_state* GameState = (game_state*)Memory->PermanentStorage;

    if(!Memory->IsInitialized)
    {
        GameState->PlayerP.AbsTileX = 3;
        GameState->PlayerP.AbsTileY = 2;
        GameState->PlayerP.OffsetX = 0.0f;
        GameState->PlayerP.OffsetY = 0.0f;
        InitializeArena(&GameState->WorldArena, Memory->PermanentStorageSize - sizeof(game_state),
                        (uint8*)Memory->PermanentStorage + sizeof(game_state));

        GameState->World = PushSize(&GameState->WorldArena, world);
        world* World = GameState->World;
        World->TileMap = PushSize(&GameState->WorldArena, tile_map);
        
        tile_map* TileMap = World->TileMap;

        TileMap->ChunkShift = 4;
        TileMap->ChunkMask = (1 << TileMap->ChunkShift) - 1;
        TileMap->ChunkDim = (1 << TileMap->ChunkShift);

        TileMap->TileChunkCountX = 128;
        TileMap->TileChunkCountY = 128;
        TileMap->TileChunkCountZ = 2;

        TileMap->TileChunks = PushArray(&GameState->WorldArena, 
                                        TileMap->TileChunkCountX*
                                        TileMap->TileChunkCountY*
                                        TileMap->TileChunkCountZ,
                                        tile_chunk);

        TileMap->TileSideInMeters = 1.5f;
        

        

        uint32 RandomNumberIndex = 0;

        uint32 TilesPerWidth = 17;
        uint32 TilesPerHeight = 9;
        uint32 ScreenX = 0;
        uint32 ScreenY = 0;

        bool32 DoorLeft = false;
        bool32 DoorRight = false;
        bool32 DoorTop = false;
        bool32 DoorBottom = false;
        bool32 DoorUp = false;
        bool32 DoorDown = false;
        uint32 AbsTileZ = 0;

        for (uint32 ScreenIndex = 0;
             ScreenIndex < 100;
             ++ScreenIndex)
        {
            Assert(RandomNumberIndex < ArrayCount(RandomNumberTable));
            uint32 RandomChoice;
            if (DoorUp || DoorDown)
            {
                RandomChoice = RandomNumberTable[RandomNumberIndex++] % 2;
            }
            else
            {
                RandomChoice = RandomNumberTable[RandomNumberIndex++] % 3;
            }

            bool32 CreatedZDoor = false;
            if (RandomChoice == 2)
            {
                if (AbsTileZ == 0)
                {
                    DoorUp = true;
                }
                else
                {
                    DoorDown = true;
                }
                CreatedZDoor = true;
            }
            else if (RandomChoice == 1)
            {
                DoorRight = true;
            }
            else
            {
                DoorTop = true;
            }

            for (uint32 TileY = 0;
                TileY < TilesPerHeight;
                ++TileY)
            {
                for (uint32 TileX = 0;
                    TileX < TilesPerWidth;
                    ++TileX)
                {
                    uint32 AbsTileX = ScreenX * TilesPerWidth + TileX;
                    uint32 AbsTileY = ScreenY * TilesPerHeight + TileY;

                    uint32 TileValue = 1;
                    if (TileX == 0)
                    {
                        if ((TileY == (TilesPerHeight / 2)) && DoorLeft)
                        {
                            TileValue = 1;
                        }
                        else
                        {
                            TileValue = 2;
                        }
                    }
                    if(TileX == TilesPerWidth - 1)
                    {
                        if ((TileY == (TilesPerHeight / 2)) && DoorRight)
                        {
                            TileValue = 1;
                        }
                        else
                        {
                            TileValue = 2;
                        }
                    }
                    if (TileY == 0)
                    {
                        if ((TileX == (TilesPerWidth / 2)) && DoorBottom)
                        {
                            TileValue = 1;
                        }
                        else
                        {
                            TileValue = 2;
                        }
                    }
                    if (TileY == TilesPerHeight - 1)
                    {
                        if ((TileX == (TilesPerWidth / 2)) && DoorTop)
                        {
                            TileValue = 1;
                        }
                        else
                        {
                            TileValue = 2;
                        }
                    }
                    if ((TileX == 5) && (TileY == 5))
                    {
                        if (DoorUp)
                        {
                            TileValue = 3;
                        }
                        else if (DoorDown)
                        {
                            TileValue = 4;
                        }
                    }

                    SetTileValue(&GameState->WorldArena, World->TileMap, AbsTileX, AbsTileY, AbsTileZ, TileValue);
                }
            }
            
            DoorLeft = DoorRight;
            DoorBottom = DoorTop;
            if (CreatedZDoor)
            {

                DoorDown = !DoorDown;
                DoorUp = !DoorUp;
            }
            else
            {
                DoorDown = false;
                DoorUp = false;
            }

            DoorRight = false;
            DoorTop = false;

            if (RandomChoice == 2)
            {
                if (AbsTileZ == 0)
                {
                    AbsTileZ = 1;
                }
                else
                {
                    AbsTileZ = 0;
                }
            }
            else if (RandomChoice == 1)
            {
                ScreenX += 1;
            }
            else
            {
                ScreenY += 1;
            }
        }
        
        // TODO(Sebas): This may be more appropriate to do in the platform layer
        Memory->IsInitialized = true;
    }
    world* World = GameState->World;
    tile_map* TileMap = World->TileMap;

    uint32 TileSideInPixels = 60;
    real32 MetersToPixels = (real32)TileSideInPixels / (real32)TileMap->TileSideInMeters;
    
    real32 LowerLeftX = -(real32)TileSideInPixels / 2.0f;
    real32 LowerLeftY = (real32)Buffer->Height;

    real32 PlayerHeight = 1.4f;
    real32 PlayerWidth = 0.75f * PlayerHeight;


    for (int ControllerIndex = 0; 
         ControllerIndex < ArrayCount(Input->Controllers);
         ControllerIndex++)
    {
        game_controller_input* Controller = GetController(Input, ControllerIndex);
        if (Controller->IsConnected)
        {
            if (Controller->IsAnalog)
            {
            }
            else
            {
                real32 dPlayerX = 0.0f;
                real32 dPlayerY = 0.0f;
                if (Controller->MoveUp.EndedDown)
                {
                    dPlayerY = 1.0f;
                }
                if (Controller->MoveDown.EndedDown)
                {
                    dPlayerY = -1.0f;
                }
                if (Controller->MoveLeft.EndedDown)
                {
                    dPlayerX = -1.0f;
                }
                if (Controller->MoveRight.EndedDown)
                {
                    dPlayerX = 1.0f;
                }
                real32 PlayerSpeed = 2.0f;

                if (Controller->ActionUp.EndedDown)
                {
                    PlayerSpeed = 10.0f;
                }
                dPlayerX *= PlayerSpeed;
                dPlayerY *= PlayerSpeed;

                tile_map_position NewPlayerP = GameState->PlayerP;
                NewPlayerP.OffsetX += dPlayerX * Input->dtPerFrame;
                NewPlayerP.OffsetY += dPlayerY * Input->dtPerFrame;
                NewPlayerP = RecannonicalizePosition(TileMap, NewPlayerP);

                tile_map_position PlayerLeft = NewPlayerP;
                PlayerLeft.OffsetX -= 0.5f * PlayerWidth;
                PlayerLeft = RecannonicalizePosition(TileMap, PlayerLeft);
                tile_map_position PlayerRight = NewPlayerP;
                PlayerRight.OffsetX += 0.5f * PlayerWidth;
                PlayerRight = RecannonicalizePosition(TileMap, PlayerRight);
                if (IsTileMapPointEmpty(TileMap, NewPlayerP) &&
                    IsTileMapPointEmpty(TileMap, PlayerLeft) &&
                    IsTileMapPointEmpty(TileMap, PlayerRight))
                {
                    if (!AreOnSameTile(GameState->PlayerP, NewPlayerP))
                    {
                        uint32 NewTileValue = GetTileValue(TileMap, NewPlayerP);
                        if (NewTileValue == 3)
                        {
                            NewPlayerP.AbsTileZ++;
                        }
                        else if (NewTileValue == 4)
                        {
                            NewPlayerP.AbsTileZ--;
                        }
                    }
                    GameState->PlayerP = NewPlayerP;
                }

            }
        }
    }

    /*tile_chunk_position PlayerChunkP = GetChunkPositionFor(&World, GameState->PlayerP.AbsTileX, GameState->PlayerP.AbsTileY);
    tile_chunk* NewTileChunk = GetTileChunk(&World, PlayerChunkP.TileChunkX, PlayerChunkP.TileChunkY);
    Assert(NewTileChunk);*/

    DrawRectangle(Buffer, 0.0f, 0.0f, (real32)Buffer->Width, (real32)Buffer->Height, 
                  1.0f, 0.0f, 1.0f);

    real32 ScreenCenterX = 0.5f * (real32)Buffer->Width;
    real32 ScreenCenterY = 0.5f * (real32)Buffer->Height;

    for (int32 RelRow =  -10; RelRow < 10; RelRow++)
    {
        for (int32 RelColumn = -20; RelColumn < 20; RelColumn++)
        {
            uint32 Column = GameState->PlayerP.AbsTileX + RelColumn;
            uint32 Row = GameState->PlayerP.AbsTileY + RelRow;

            uint32 TileID = GetTileValue(TileMap, Column, Row, GameState->PlayerP.AbsTileZ);
            if (TileID > 0)
            {
                real32 Gray = 0.5f;
                if (TileID == 2)
                {
                    Gray = 1.0f;
                }

                if (TileID > 2)
                {
                    Gray = 0.25f;
                }

                if ((Column == GameState->PlayerP.AbsTileX) && (Row == GameState->PlayerP.AbsTileY))
                {
                    Gray = 0.0f;
                }

                real32 CenterX = ScreenCenterX - MetersToPixels * GameState->PlayerP.OffsetX
                    + ((real32)RelColumn) * TileSideInPixels;
                real32 CenterY = ScreenCenterY + MetersToPixels * GameState->PlayerP.OffsetY
                    - ((real32)RelRow) * TileSideInPixels;
                real32 MinX = CenterX - 0.5f * TileSideInPixels;
                real32 MinY = CenterY - 0.5f * TileSideInPixels;
                real32 MaxX = CenterX + 0.5f * TileSideInPixels;
                real32 MaxY = CenterY + 0.5f * TileSideInPixels;
                DrawRectangle(Buffer, MinX, MinY, MaxX, MaxY, Gray, Gray, Gray);
            }
        }
    }

    real32 PlayerR = 1.0f;
    real32 PlayerG = 1.0f;
    real32 PlayerB = 0.0f;
    real32 PlayerLeft = ScreenCenterX - PlayerWidth * 0.5f * MetersToPixels;
    real32 PlayerTop = ScreenCenterY - PlayerHeight * MetersToPixels;
    DrawRectangle(Buffer, 
                  PlayerLeft, PlayerTop, 
                  PlayerLeft + PlayerWidth * MetersToPixels, 
                  PlayerTop + PlayerHeight * MetersToPixels, 
                  PlayerR, PlayerG, PlayerB);
}

